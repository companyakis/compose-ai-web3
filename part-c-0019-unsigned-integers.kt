fun main() {
    
    // unsigned integers -> not negative numbers!
    
    val age: UByte = 35u 
    
    val year: UShort = 2025u 
    
    val budget: UInt = 580000u 
    
    var totalSales: ULong = 52500000000u
    
    println(age)
    println(year)
    println(budget)
    println(totalSales)
    
// 35
// 2025
// 580000
// 52500000000
    
}
